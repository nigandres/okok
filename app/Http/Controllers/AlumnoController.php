     */
    public function store(Request $request)
    {
        $alumno = new Alumno();
        $alumno->nombre = $request->input('nombre');
        $alumno->codigo = $request->codigo;
        $alumno->carrera = $request->carrera;
        $alumno->save();
        return redirect()->route('alumno.index');
    }

